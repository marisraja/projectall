package org.ctrl;

public class Switch {
	public static void main(String[] args) {
		int height=1;
		switch (height) {
		case 1:
			System.out.println("person:1"+100);
			
			break;
		case 2:
			System.out.println("person:2."+200);
			
			break;

		default:
			System.out.println("not");
			break;
		}
		
	}
}
  